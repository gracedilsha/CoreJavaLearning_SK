public class EX_Loops_for_findEven {
    public static void main(String[] args) {

        int num =50;

        for(int i=0; i<=num; i++){

            if(i%2==0)
            {
                System.out.println(i +" is Even Number ");
            }

                   }
        System.out.println("End of Program");

    }
}
