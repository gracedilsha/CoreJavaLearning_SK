package src.Basics_Jan14;

public class Casting {

    public static void main(String[] args) {


        int a = 10;
        double d = a;

        System.out.println( a);
        System.out.println( d);

        double d1 = 101.0;
        int a1 = (int )d1;

        System.out.println( d1);
        System.out.println( a1);
    }
}
